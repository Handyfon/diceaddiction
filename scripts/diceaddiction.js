function applyDiceAddictionThrowJellyState(box, throws, persistentThrowData) {
	const engine = box?.throwEngine;
	if (!engine) return false;

	let diceCount = 0;
	let jellyCount = 0;
	const samples = [];
	const throwCount = Array.isArray(throws) ? throws.length : 0;
	const persistentCount = Array.isArray(persistentThrowData?.heldDice) ? persistentThrowData.heldDice.length : 0;
	console.warn("Dice Addiction | Jelly sound batch entry", {
		throwCount,
		persistentCount,
		hasBox: !!box,
		hasEngine: !!engine
	});
	for (const notation of Array.isArray(throws) ? throws : []) {
		for (const die of Array.isArray(notation?.dice) ? notation.dice : []) {
			diceCount++;
			const appearance = getDiceAddictionThrowAppearance(box, notation, die);
			if (samples.length < 8) {
				samples.push({
					type: die?.type,
					appearanceMaterial: appearance?.material,
					appearanceSystem: appearance?.system,
					appearanceColorset: appearance?.colorset,
					optionMaterial: die?.options?.appearance?.material,
					optionSystem: die?.options?.appearance?.system,
					markedOption: !!die?.options?.diceAddictionJellySound
				});
			}
			if (isDiceAddictionJellyAppearance(appearance) || isDiceAddictionJellyAppearance(die?.options?.appearance)) {
				jellyCount++;
				die.options ||= {};
				die.options.diceAddictionJellySound = true;
			}
		}
	}

	const heldDice = Array.isArray(persistentThrowData?.heldDice) ? persistentThrowData.heldDice.filter(Boolean) : [];
		for (const die of heldDice) {
		diceCount++;
		if (isDiceAddictionJellySoundDice(die) || hasDiceAddictionJellyMaterial(die)) {
			jellyCount++;
			markDiceAddictionJellySoundDice(die);
		}
	}

	engine._diceAddictionPreBatchDiceCount = diceCount;
	engine._diceAddictionPreBatchJellyCount = jellyCount;
	const helperAllowance = Math.max(0, throwCount + persistentCount);
	const nonJellyCount = Math.max(0, diceCount - jellyCount);
	engine._diceAddictionPreBatchAllJelly = diceCount > 0 && jellyCount === diceCount;
	engine._diceAddictionPreBatchJellyWithHelpers = jellyCount > 0 && nonJellyCount <= helperAllowance;
	diceAddictionJellySoundDiagnostics.lastState = {
		diceCount,
		jellyCount,
		nonJellyCount,
		helperAllowance,
		allJelly: engine._diceAddictionPreBatchAllJelly,
		jellyWithHelpers: engine._diceAddictionPreBatchJellyWithHelpers,
		samples
	};
	if (diceCount > 0) {
		console.warn("Dice Addiction | Jelly sound pre-batch state", diceAddictionJellySoundDiagnostics.lastState);
	}
	return engine._diceAddictionPreBatchAllJelly || engine._diceAddictionPreBatchJellyWithHelpers;
}

function updateDiceAddictionJellyDice(dice, now, box = null) {
	markDiceAddictionJellyDice(dice);
	const state = dice?.userData?.diceAddictionJelly;
	if (!state) return false;

	const carrier = dice.parent || dice;
	const position = carrier.position;
	const quaternion = carrier.quaternion || dice.quaternion;
	const dt = Math.min(Math.max((now - state.lastTime) / 1000, 1 / 120), 1 / 20);
	state.lastTime = now;

	if (!state.lastPosition) {
		state.lastPosition = position.clone();
		state.lastQuaternion = quaternion?.clone?.() || null;
		return false;
	}

	const delta = position.clone().sub(state.lastPosition);
	const distance = delta.length();
	const speed = distance / dt;
	const verticalVelocity = delta.y / dt;
	let angularSpeed = 0;
	if (state.lastQuaternion && quaternion?.dot) {
		const dot = Math.min(Math.abs(quaternion.dot(state.lastQuaternion)), 1);
		angularSpeed = (2 * Math.acos(dot)) / dt;
	}

	const dragTarget = state.dragUntil > now ? (state.dragWobble || 0) : 0;
	const motionTarget = speed * 0.00135 + angularSpeed * 0.04 + Math.abs(speed - state.lastSpeed) * 0.00042;
	if (motionTarget > 0.012 || state.wobble > 0.04) {
		state.restWobble = Math.max(state.restWobble || 0, Math.min(0.08, Math.max(motionTarget * 0.34, state.wobble * 0.24)));
		state.restUntil = now + 1500;
	}
	const restTarget = state.restUntil > now ? (state.restWobble || 0) : 0;
	const target = Math.min(0.48, Math.max(dragTarget, motionTarget, restTarget));
	state.wobble += (target - state.wobble) * Math.min(1, dt * 8);
	if (target < 0.01) state.wobble *= Math.max(0, 1 - dt * 0.45);
	if (state.dragWobble > 0) {
		state.dragWobble *= Math.max(0, 1 - dt * (state.dragUntil > now ? 1.4 : 5));
		if (state.dragWobble < 0.001) state.dragWobble = 0;
	}
	if (state.restWobble > 0) {
		state.restWobble *= Math.max(0, 1 - dt * (state.restUntil > now ? 1.35 : 4.2));
		if (state.restWobble < 0.001) state.restWobble = 0;
	}
	state.phase += dt * (4.6 + state.wobble * 18);

	if (distance > 0.001) {
		state.direction = delta.normalize();
	} else if (!state.direction) {
		state.direction = { x: 0, y: 1, z: 0 };
	}

	const localDirection = state.direction.clone?.() || position.clone().set(state.direction.x, state.direction.y, state.direction.z);
	const inverseQuaternion = quaternion?.clone?.();
	inverseQuaternion?.invert?.();
	if (inverseQuaternion) localDirection.applyQuaternion(inverseQuaternion);
	localDirection.normalize?.();

	const pulse = state.wobble + Math.sin(state.phase) * state.wobble * 0.5;
	const compression = 1 - pulse * 0.68;
	const stretch = pulse * 1.32;
	const nx = Math.abs(localDirection.x || 0);
	const ny = Math.abs(localDirection.y || 0);
	const nz = Math.abs(localDirection.z || 0);

	dice.scale.set(
		state.baseScale.x * clampDiceAddictionJellyScale(compression + stretch * nx),
		state.baseScale.y * clampDiceAddictionJellyScale(compression + stretch * ny),
		state.baseScale.z * clampDiceAddictionJellyScale(compression + stretch * nz)
	);

	if (state.wobble < 0.0015 && speed < 0.01) {
		dice.scale.copy(state.baseScale);
	}

	maybeCreateDiceAddictionJellyPuddle(box, dice, position, verticalVelocity, speed, now);
	state.lastVelocityY = verticalVelocity;
	state.lastPosition.copy(position);
	state.lastQuaternion?.copy?.(quaternion);
	state.lastSpeed = speed;
	return state.wobble >= 0.0015 || speed >= 0.01 || state.restUntil > now;
}

function updateDiceAddictionJellyDiceInScene(scene, box = null) {
	if (!scene?.children?.length) return false;
	const now = performance.now();
	let active = updateDiceAddictionJellyPuddles(box, now);
	for (const child of scene.children) {
		const dice = child?.children?.[0] || child;
		if (dice?.isMesh || dice?.type === "Mesh" || dice?.children?.length) {
			active = updateDiceAddictionJellyDice(dice, now, box) || active;
		}
	}
	return active;
}

function collectDiceAddictionJellyDraggedDice(inputHandler) {
	const dice = [];
	if (Array.isArray(inputHandler?.mouse?.heldPersistentDice)) {
		dice.push(...inputHandler.mouse.heldPersistentDice);
	}

	const constrainedId = inputHandler?.mouse?.constrainedEphemeralId;
	if (constrainedId !== undefined && constrainedId !== null) {
		const engineDice = [
			...(Array.isArray(inputHandler?.throwEngine?.diceList) ? inputHandler.throwEngine.diceList : []),
			...(Array.isArray(inputHandler?.throwEngine?.deadDiceList) ? inputHandler.throwEngine.deadDiceList : [])
		];
		const constrainedDie = engineDice.find((die) => die?.id === constrainedId || die?.userData?.id === constrainedId);
		if (constrainedDie) dice.push(constrainedDie);
	}

	return dice;
}

function isDiceAddictionJellyCollision(data) {
	const material = data?.diceMaterial || data?.material;
	if (material === "diceaddiction-jelly") return true;
	return !!data?.diceAddictionJelly;
}

function isDiceAddictionCurrentRollAllJelly(box) {
	const engine = box?.throwEngine;
	if (engine?._diceAddictionPlaybackAllJelly) return true;
	if (engine?._diceAddictionLastWorkerBatchAllJelly) return true;
	if (engine?._diceAddictionPreBatchAllJelly) return true;
	if (engine?._diceAddictionPreBatchJellyWithHelpers) return true;
	const ephemeralCount = Number(engine?._diceAddictionEphemeralThrowDiceCount) || 0;
	if (ephemeralCount > 0) {
		return Number(engine?._diceAddictionEphemeralThrowJellyCount) === ephemeralCount
			&& (!Number(engine?._diceAddictionPersistentThrowDiceCount) || !!engine?._diceAddictionPersistentThrowAllJelly);
	}

	const dice = Array.isArray(engine?.diceList) ? engine.diceList.filter(Boolean) : [];
	const persistentCount = Number(engine?._diceAddictionPersistentThrowDiceCount) || 0;
	if (!dice.length && !persistentCount) return false;
	return (!dice.length || dice.every((die) => isDiceAddictionJellySoundDice(die)))
		&& (!persistentCount || !!engine?._diceAddictionPersistentThrowAllJelly);
}

function collectDiceAddictionLiveDice(box) {
	const dice = [];
	const engine = box?.throwEngine;
	if (Array.isArray(engine?.diceList)) dice.push(...engine.diceList.filter(Boolean));
	if (Array.isArray(engine?.deadDiceList)) dice.push(...engine.deadDiceList.filter(Boolean));
	if (Array.isArray(box?.persistentDiceList)) dice.push(...box.persistentDiceList.filter(Boolean));
	const scene = box?.scene || box?.diceScene?.scene;
	for (const child of scene?.children || []) {
		const die = child?.children?.[0] || child;
		if (die && !dice.includes(die) && (die.isMesh || die.type === "Mesh" || die.children?.length)) dice.push(die);
	}
	return dice;
}

function hasOnlyLiveJellyDice(box) {
	const dice = collectDiceAddictionLiveDice(box);
	const relevantDice = dice.filter((die) => isDiceAddictionJellySoundDice(die) || hasDiceAddictionJellyMaterial(die) || die?.sim);
	return relevantDice.length > 0 && relevantDice.every((die) => isDiceAddictionJellySoundDice(die) || hasDiceAddictionJellyMaterial(die));
}

function hasOnlyDiceAddictionJellyDice(dice) {
	const heldDice = Array.isArray(dice) ? dice.filter(Boolean) : [];
	return heldDice.length > 0 && heldDice.every((die) => isDiceAddictionJellySoundDice(die) || hasDiceAddictionJellyMaterial(die));
}

function markDiceAddictionJellyInteraction(box, dice, ttl = DICE_ADDICTION_JELLY_INTERACTION_SOUND_TTL) {
	if (!box || !hasOnlyDiceAddictionJellyDice(dice)) return false;
	const engine = box.throwEngine;
	if (!engine) return false;
	for (const die of dice) markDiceAddictionJellySoundDice(die);
	engine._diceAddictionJellyInteractionUntil = performance.now() + ttl;
	engine._diceAddictionJellyInteractionCount = dice.length;
	return true;
}

function isDiceAddictionJellyInteractionActive(box) {
	const engine = box?.throwEngine;
	return Number(engine?._diceAddictionJellyInteractionUntil) > performance.now();
}

function shouldUseDiceAddictionJellySound(data, box) {
	if (isDiceAddictionJellyCollision(data)) return true;
	if (isDiceAddictionCurrentRollAllJelly(box)) return true;
	if (isDiceAddictionJellyInteractionActive(box)) return true;
	if (hasOnlyLiveJellyDice(box)) return true;
	if (data?.source !== "table") return false;
	return false;
}

