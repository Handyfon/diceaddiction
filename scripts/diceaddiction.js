function normalizeDiceAddictionColorValue(value) {
	if (Array.isArray(value)) {
		const parsedColors = value.map(normalizeDiceAddictionColorValue).filter(Boolean);
		return parsedColors.find((color) => Math.max(color.r, color.g, color.b) > 0.02) || parsedColors[0] || null;
	}
	if (typeof value === "string") {
		const match = value.trim().match(/^#?([0-9a-f]{3}|[0-9a-f]{6})$/i);
		if (!match) return null;
		const hex = match[1].length === 3
			? match[1].split("").map((char) => char + char).join("")
			: match[1];
		const intValue = Number.parseInt(hex, 16);
		return {
			r: ((intValue >> 16) & 255) / 255,
			g: ((intValue >> 8) & 255) / 255,
			b: (intValue & 255) / 255
		};
	}
	if (value && Number.isFinite(value.r) && Number.isFinite(value.g) && Number.isFinite(value.b)) {
		return { r: value.r, g: value.g, b: value.b };
	}
	return null;
}

function getDiceAddictionBaseEmissionColor(material, appearance) {
	const appearanceColor = normalizeDiceAddictionColorValue(appearance?.diceColor);
	if (appearanceColor) return appearanceColor;

	const materialAppearanceColor = normalizeDiceAddictionColorValue(material?.userData?.appearance?.diceColor);
	if (materialAppearanceColor) return materialAppearanceColor;

	return normalizeDiceAddictionColorValue(material?.color) || { r: 0.18, g: 0.18, b: 0.18 };
}

function getDiceAddictionFontEmissionColor(dice3d, material, appearance) {
	const colorset = getDiceAddictionColorset(dice3d, appearance);
	const colorSources = [
		appearance?.labelColor,
		material?.userData?.appearance?.labelColor,
		appearance?.foreground,
		material?.userData?.appearance?.foreground,
		colorset?.foreground,
		colorset?.labelColor
	];
	for (const source of colorSources) {
		const color = normalizeDiceAddictionColorValue(source);
		if (color) return color;
	}
	return { r: 1, g: 1, b: 1 };
}

