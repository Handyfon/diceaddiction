function buildDiceAddictionCriticalEmissiveMap(THREE, sourceTexture, mode) {
	if (!THREE?.CanvasTexture) return null;
	const source = sourceTexture?.image;
	if (!source?.width || !source?.height) return null;

	const canvas = document.createElement("canvas");
	canvas.width = source.width;
	canvas.height = source.height;
	const context = canvas.getContext("2d");
	context.fillStyle = "#000000";
	context.fillRect(0, 0, canvas.width, canvas.height);

	const tileSize = source.width / 5;
	const copyTile = (index) => {
		const tilesPerLine = 5;
		const sx = (index % tilesPerLine) * tileSize;
		const sy = Math.floor(index / tilesPerLine) * tileSize;
		context.drawImage(source, sx, sy, tileSize, tileSize, sx, sy, tileSize, tileSize);
	};

	if (mode === "lowest" || mode === "both") copyTile(2);
	if (mode === "highest" || mode === "both") copyTile(21);

	const texture = new THREE.CanvasTexture(canvas);
	if (THREE.SRGBColorSpace) texture.colorSpace = THREE.SRGBColorSpace;
	texture.flipY = false;
	return texture;
}

function shouldSuppressDiceAddictionCoinEmission(diceType, material, appearance) {
	const normalizedType = String(diceType || material?.userData || "").toLowerCase();
	const isCoinLike = ["d2", "dc", "coin"].includes(normalizedType);
	if (!isCoinLike) return false;

	const colorset = String(appearance?.colorset || "").toLowerCase();
	const texture = String(appearance?.texture || appearance?.textureType || "").toLowerCase();
	return colorset === "coin_default" || !texture || texture === "none" || texture === "metal";
}

