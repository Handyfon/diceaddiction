function getDiceAddictionPresetControlValue(control) {
	const type = String(control.type || "").toLowerCase();
	if (control.tagName === "SELECT" && control.multiple) {
		return [...control.selectedOptions].map((option) => option.value);
	}
	return control.value;
}

function updateDiceAddictionRangeLabel(control) {
	if (String(control.type || "").toLowerCase() !== "range") return;
	const rangeValue = control.closest(".form-fields")?.querySelector(".range-value");
	if (rangeValue) rangeValue.textContent = control.value;
}

function setDiceAddictionPresetControlValue(control, field) {
	const type = String(control.type || "").toLowerCase();
	if (type === "checkbox") {
		control.checked = field.checked ?? !!field.value;
		return;
	}
	if (type === "radio") {
		if (field.checked === undefined) {
			if (String(control.value) === String(field.value)) control.checked = true;
			return;
		}
		if (String(control.value) === String(field.value)) control.checked = !!field.checked;
		return;
	}
	if (control.tagName === "SELECT" && control.multiple && Array.isArray(field.value)) {
		for (const option of control.options) option.selected = field.value.includes(option.value);
		return;
	}
	control.value = field.value ?? "";
}

function syncDiceAddictionPresetPairedInputs(root = document) {
	const controls = getDiceAddictionPresetControls(root);
	for (const control of controls) {
		if (String(control.type || "").toLowerCase() !== "color" || !control.dataset.edit) continue;
		const source = controls.find((candidate) => candidate.name === control.dataset.edit);
		if (source?.value && /^#[0-9a-f]{6}$/i.test(source.value)) control.value = source.value;
	}
	for (const control of controls) updateDiceAddictionRangeLabel(control);
}

