function mapDiceAddictionBloomAmount(value) {
	const amount = Math.min(Math.max(Number(value) || 0, 0), 2);
	return Math.pow(amount / 2, 2.2) * 2;
}

function normalizeDiceAddictionTextureEmissionMode(value) {
	const normalized = String(value || "").toLowerCase().replace(/[^a-z]/g, "");
	const modes = {
		equal: "equal",
		brightness: "brightness",
		bybrightness: "brightness",
		saturation: "saturation",
		bycolorsaturation: "saturation",
		edge: "edge",
		fromedge: "edge",
		center: "center",
		fromcenter: "center"
	};
	return modes[normalized] || "equal";
}

function readDiceAddictionEmissionSettings(settings = {}) {
	const numberSetting = (id, fallback) => {
		const value = Number(settings[id]);
		return Number.isFinite(value) ? value : fallback;
	};

	return {
		textureEnabled: !!settings.diceAddictionTextureEmission,
		textureMode: normalizeDiceAddictionTextureEmissionMode(settings.diceAddictionTextureEmissionMode),
		textureStrength: numberSetting("diceAddictionTextureEmissionStrength", 0.75),
		textureBloomStrength: mapDiceAddictionBloomAmount(numberSetting("diceAddictionTextureEmissionBloomStrength", 1)),
		textureThreshold: numberSetting("diceAddictionTextureEmissionThreshold", 0.4),
		textureRadius: numberSetting("diceAddictionTextureEmissionRadius", 0.04),
		textureBlur: numberSetting("diceAddictionTextureEmissionBlur", 1),
		fontEnabled: settings.diceAddictionFontEmission !== false,
		fontStrength: numberSetting("diceAddictionFontEmissionStrength", 1.2)
	};
}

