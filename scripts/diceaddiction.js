function extractDiceAddictionSettingsFromSource(source) {
	const settings = {};
	if (!source || typeof source !== "object") return settings;

	const merge = (candidate) => {
		if (!candidate || typeof candidate !== "object") return;
		for (const [key, value] of Object.entries(candidate)) {
			if (key.startsWith("diceAddiction")) settings[key] = value;
		}
	};

	merge(source.systemSettings);
	merge(source.diceAddiction);
	merge(source.diceAddictionSettings);
	merge(source);
	return settings;
}

