function getDiceAddictionGeometryFaceAlignmentMin(geometry, center = null) {
	const position = geometry?.attributes?.position;
	if (!position?.count) return 0.58;

	// Match the shader's model-space face metric using real triangle planes.
	const index = geometry.index;
	const triangleCount = Math.floor((index?.count || position.count) / 3);
	const centerX = center?.x || 0;
	const centerY = center?.y || 0;
	const centerZ = center?.z || 0;
	let minimumAlignment = 1;
	let samples = 0;

	const getVertex = (vertexIndex) => ({
		x: position.getX(vertexIndex),
		y: position.getY(vertexIndex),
		z: position.getZ(vertexIndex)
	});
	const getTriangleIndex = (triangleOffset) => index ? index.getX(triangleOffset) : triangleOffset;

	for (let triangle = 0; triangle < triangleCount; triangle++) {
		const a = getVertex(getTriangleIndex(triangle * 3));
		const b = getVertex(getTriangleIndex((triangle * 3) + 1));
		const c = getVertex(getTriangleIndex((triangle * 3) + 2));
		const ab = { x: b.x - a.x, y: b.y - a.y, z: b.z - a.z };
		const ac = { x: c.x - a.x, y: c.y - a.y, z: c.z - a.z };
		let normal = {
			x: ab.y * ac.z - ab.z * ac.y,
			y: ab.z * ac.x - ab.x * ac.z,
			z: ab.x * ac.y - ab.y * ac.x
		};
		const normalLength = Math.hypot(normal.x, normal.y, normal.z);
		if (normalLength <= 0.000001) continue;
		normal.x /= normalLength;
		normal.y /= normalLength;
		normal.z /= normalLength;

		const centroid = {
			x: ((a.x + b.x + c.x) / 3) - centerX,
			y: ((a.y + b.y + c.y) / 3) - centerY,
			z: ((a.z + b.z + c.z) / 3) - centerZ
		};
		if ((normal.x * centroid.x + normal.y * centroid.y + normal.z * centroid.z) < 0) {
			normal = { x: -normal.x, y: -normal.y, z: -normal.z };
		}

		for (const vertex of [a, b, c]) {
			const x = vertex.x - centerX;
			const y = vertex.y - centerY;
			const z = vertex.z - centerZ;
			const vertexLength = Math.hypot(x, y, z);
			if (vertexLength <= 0.000001) continue;
			const alignment = Math.abs((x * normal.x + y * normal.y + z * normal.z) / vertexLength);
			if (!Number.isFinite(alignment)) continue;
			minimumAlignment = Math.min(minimumAlignment, alignment);
			samples += 1;
		}
	}

	if (!samples) return 0.58;
	return Math.min(Math.max(minimumAlignment, 0.02), 0.97);
}

function updateDiceAddictionMaterialGeometryUniforms(material) {
	const shader = material?.userData?.diceAddictionShader;
	if (!shader?.uniforms) return;

	if (shader.uniforms.diceAddictionModelCenter) {
		shader.uniforms.diceAddictionModelCenter.value = material.userData.diceAddictionModelCenter || { x: 0, y: 0, z: 0 };
	}
	if (shader.uniforms.diceAddictionFaceAlignmentMin) {
		shader.uniforms.diceAddictionFaceAlignmentMin.value = material.userData.diceAddictionFaceAlignmentMin ?? 0.58;
	}
}

function setDiceAddictionMaterialModelRadius(material, radius, center = null, faceAlignmentMin = null) {
	const modelRadius = Number(radius);
	if (!material?.userData || !Number.isFinite(modelRadius) || modelRadius <= 0) return;

	material.userData.diceAddictionModelCenter = center?.clone?.() || center || { x: 0, y: 0, z: 0 };
	const modelFaceAlignmentMin = Number(faceAlignmentMin);
	if (Number.isFinite(modelFaceAlignmentMin)) {
		material.userData.diceAddictionFaceAlignmentMin = Math.min(Math.max(modelFaceAlignmentMin, 0.02), 0.97);
	}
	updateDiceAddictionMaterialGeometryUniforms(material);
}

function applyDiceAddictionLiveModelRadius(dice) {
	const applyObjectRadius = (object) => {
		if (!object?.geometry || !object.material) return;
		if (!object.geometry.boundingSphere) object.geometry.computeBoundingSphere?.();
		const radius = object.geometry.boundingSphere?.radius;
		const center = object.geometry.boundingSphere?.center;
		const faceAlignmentMin = getDiceAddictionGeometryFaceAlignmentMin(object.geometry, center);
		forEachDiceAddictionMaterial(object.material, (material) => {
			setDiceAddictionMaterialModelRadius(material, radius, center, faceAlignmentMin);
		});
	};

	if (typeof dice?.traverse === "function") {
		dice.traverse(applyObjectRadius);
	} else {
		applyObjectRadius(dice);
	}
}

function applyDiceAddictionPreviewModelRadii(app) {
	const diceList = app?.showcaseView?.diceList;
	if (!Array.isArray(diceList)) return;
	for (const dice of diceList) applyDiceAddictionLiveModelRadius(dice);
}

function markDiceAddictionPreviewEmissionMaterialsDirty(app) {
	const diceList = app?.showcaseView?.diceList;
	if (!Array.isArray(diceList)) return false;

	let hasDiceAddictionMaterial = false;
	for (const dice of diceList) {
		dice.traverse?.((object) => {
			forEachDiceAddictionMaterial(object.material, (material) => {
				if (!material?.userData?.diceAddictionEmission) return;
				hasDiceAddictionMaterial = true;
				if (!material.userData.diceAddictionShader) {
					material.needsUpdate = true;
				}
			});
		});
	}
	return hasDiceAddictionMaterial;
}

function refreshDiceAddictionPreviewEmission(app) {
	if (!app) return false;
	suppressDiceAddictionPreviewCoinEmission(app);
	applyDiceAddictionPreviewModelRadii(app);
	const hasDiceAddictionMaterial = markDiceAddictionPreviewEmissionMaterialsDirty(app);
	app.diceScene?.renderScene?.();
	return hasDiceAddictionMaterial || Array.isArray(app.showcaseView?.diceList);
}

function scheduleDiceAddictionPreviewEmissionRefresh(app, attempts = 8) {
	if (!app || diceAddictionPreviewRefreshApps.has(app)) return;
	diceAddictionPreviewRefreshApps.add(app);

	let remainingAttempts = attempts;
	const refresh = () => {
		refreshDiceAddictionPreviewEmission(app);
		remainingAttempts -= 1;
		if (remainingAttempts <= 0) {
			diceAddictionPreviewRefreshApps.delete(app);
			return;
		}
		window.setTimeout(refresh, remainingAttempts > attempts - 3 ? 0 : 80);
	};

	requestAnimationFrame(refresh);
}

