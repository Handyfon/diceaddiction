async function saveDiceAddictionPreset(drawer, root = document) {
	const nameInput = drawer.querySelector("[data-dice-addiction-preset-name]");
	const name = nameInput?.value?.trim();
	if (!name) {
		ui.notifications?.warn("Name the Dice Addiction preset first.");
		nameInput?.focus();
		return;
	}

	const now = new Date().toISOString();
	const presets = getDiceAddictionPresets();
	const existingIndex = presets.findIndex((preset) => preset.name?.toLowerCase() === name.toLowerCase());
	const existingPreset = existingIndex >= 0 ? presets[existingIndex] : null;
	const nextPreset = {
		id: existingPreset?.id || getDiceAddictionPresetId(),
		name,
		createdAt: existingPreset?.createdAt || now,
		updatedAt: now,
		data: collectDiceAddictionPresetData(root)
	};

	if (existingIndex >= 0) presets[existingIndex] = nextPreset;
	else presets.push(nextPreset);
	await setDiceAddictionPresets(presets);
	renderDiceAddictionPresetList(drawer);
	ui.notifications?.info(`Dice Addiction preset "${name}" saved.`);
}

async function deleteDiceAddictionPreset(drawer, presetId) {
	const presets = getDiceAddictionPresets();
	const preset = presets.find((item) => item.id === presetId);
	if (!preset) return;
	if (!window.confirm(`Delete Dice Addiction preset "${preset.name}"?`)) return;
	await setDiceAddictionPresets(presets.filter((item) => item.id !== presetId));
	renderDiceAddictionPresetList(drawer);
}

function loadDiceAddictionPreset(drawer, root, presetId) {
	const preset = getDiceAddictionPresets().find((item) => item.id === presetId);
	if (!preset) return;
	if (!applyDiceAddictionPresetData(root, preset)) {
		ui.notifications?.warn("Dice Addiction could not load that preset.");
		return;
	}
	const nameInput = drawer.querySelector("[data-dice-addiction-preset-name]");
	if (nameInput) nameInput.value = preset.name || "";
	ui.notifications?.info(`Dice Addiction preset "${preset.name}" loaded into the dialog.`);
}

function normalizeDiceAddictionImportedPresets(payload) {
	const rawPresets = Array.isArray(payload) ? payload : Array.isArray(payload?.presets) ? payload.presets : [];
	return rawPresets
		.filter((preset) => preset && typeof preset === "object" && Array.isArray(preset?.data?.fields))
		.map((preset) => ({
			id: preset.id || getDiceAddictionPresetId(),
			name: String(preset.name || "Imported preset").trim() || "Imported preset",
			createdAt: preset.createdAt || new Date().toISOString(),
			updatedAt: new Date().toISOString(),
			data: {
				version: Number(preset.data.version) || 1,
				fields: preset.data.fields
			}
		}));
}

function exportDiceAddictionPresets() {
	const presets = getDiceAddictionPresets();
	if (!presets.length) {
		ui.notifications?.warn("No Dice Addiction presets to export.");
		return;
	}

	const payload = {
		module: "diceaddiction",
		type: DICE_ADDICTION_PRESET_FLAG,
		version: 1,
		exportedAt: new Date().toISOString(),
		presets
	};
	const blob = new Blob([JSON.stringify(payload, null, 2)], { type: "application/json" });
	const url = URL.createObjectURL(blob);
	const link = document.createElement("a");
	link.href = url;
	link.download = `diceaddiction-presets-${new Date().toISOString().slice(0, 10)}.json`;
	link.style.display = "none";
	document.body.append(link);
	link.click();
	link.remove();
	window.setTimeout(() => URL.revokeObjectURL(url), 1000);
}

async function importDiceAddictionPresetsFromFile(drawer, file) {
	if (!file) return;
	let payload;
	try {
		payload = JSON.parse(await file.text());
	} catch (error) {
		ui.notifications?.error("Dice Addiction could not read that preset JSON.");
		return;
	}

	const imported = normalizeDiceAddictionImportedPresets(payload);
	if (!imported.length) {
		ui.notifications?.warn("That file did not contain Dice Addiction presets.");
		return;
	}

	const presets = getDiceAddictionPresets();
	const byId = new Map(presets.map((preset) => [preset.id, preset]));
	for (const preset of imported) {
		if (byId.has(preset.id)) preset.id = getDiceAddictionPresetId();
		byId.set(preset.id, preset);
	}
	await setDiceAddictionPresets([...byId.values()]);
	renderDiceAddictionPresetList(drawer);
	ui.notifications?.info(`Imported ${imported.length} Dice Addiction preset${imported.length === 1 ? "" : "s"}.`);
}

