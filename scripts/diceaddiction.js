function mapDiceAddictionGlassBlur(value) {
	const blur = Math.min(Math.max(Number(value) || 0, 0), 1);
	return blur < 0.35
		? blur * 0.35
		: 0.1225 + Math.pow((blur - 0.35) / 0.65, 1.7) * 0.8775;
}

function applyDiceAddictionMaterialOptions(factory, THREE) {
	if (!factory?.material_options) return;
	const withSide = (options) => THREE?.DoubleSide ? { ...options, side: THREE.DoubleSide } : options;
	const physicalMaterial = (config) => ({
		type: "physical",
		options: withSide({
			metalness: 0,
			roughness: config.roughness,
			transmission: config.transmission ?? 1,
			ior: 1.5,
			thickness: config.thickness ?? 0.04,
			attenuationDistance: config.attenuationDistance ?? 0.02,
			attenuationColor: config.attenuationColor ?? 0xf2f2ff,
			clearcoat: config.clearcoat ?? (config.bubbleStrength ? 0.65 : 0.35),
			clearcoatRoughness: config.clearcoatRoughness ?? (config.bubbleStrength ? 0.14 : 0.03),
			envMapIntensity: config.envMapIntensity ?? 1,
			specularIntensity: config.specularIntensity ?? 1
		}),
		scopedOptions: { envMap: config.envMapIntensity !== 0 }
	});
	const fallbackMaterial = (config) => ({
		type: "standard",
		options: {
			metalness: 0,
			roughness: config.roughness,
			opacity: config.opacity ?? 0.82,
			transparent: true
		},
		scopedOptions: { envMap: config.envMapIntensity !== 0 }
	});

	for (const [id, config] of Object.entries(DICE_ADDICTION_MATERIALS)) {
		factory.material_options[id] = factory.realisticLighting ? physicalMaterial(config) : fallbackMaterial(config);
	}
}

function patchDiceAddictionMaterialInitialization(factory, THREE) {
	if (!factory?.initializeMaterials || factory._diceAddictionMaterialInitializationPatched) {
		if (THREE) factory._diceAddictionThree = THREE;
		return;
	}
	factory._diceAddictionMaterialInitializationPatched = true;
	factory._diceAddictionThree = THREE || diceAddictionThree || null;
	const originalInitializeMaterials = factory.initializeMaterials;
	factory.initializeMaterials = function(...args) {
		const result = originalInitializeMaterials.apply(this, args);
		applyDiceAddictionMaterialOptions(this, this._diceAddictionThree || diceAddictionThree);
		return result;
	};
}

function clearDiceAddictionCustomMaterialCache(factory) {
	if (!factory?.baseMaterialCache) return;
	for (const [key, material] of Object.entries(factory.baseMaterialCache)) {
		const materialId = material?.userData?.materialData?.material || Object.keys(DICE_ADDICTION_MATERIALS).find((id) => key.includes(id));
		if (!DICE_ADDICTION_MATERIALS[materialId]) continue;
		material.map?.dispose?.();
		material.normalMap?.dispose?.();
		material.emissiveMap?.dispose?.();
		material.transmissionMap?.dispose?.();
		material.userData?.heightMap?.dispose?.();
		material.userData?.diceAddictionTransmissionMap?.dispose?.();
		material?.dispose?.();
		delete factory.baseMaterialCache[key];
	}
}

