function normalizeDiceAddictionVector3(vector) {
	const length = Math.hypot(vector.x, vector.y, vector.z) || 1;
	return { x: vector.x / length, y: vector.y / length, z: vector.z / length };
}

function multiplyDiceAddictionQuaternionByAxisAngle(quaternions, step, axis, angle) {
	const offset = step * 4;
	const halfAngle = angle * 0.5;
	const sin = Math.sin(halfAngle);
	const bx = axis.x * sin;
	const by = axis.y * sin;
	const bz = axis.z * sin;
	const bw = Math.cos(halfAngle);
	const ax = quaternions[offset];
	const ay = quaternions[offset + 1];
	const az = quaternions[offset + 2];
	const aw = quaternions[offset + 3];
	const x = (aw * bx) + (ax * bw) + (ay * bz) - (az * by);
	const y = (aw * by) - (ax * bz) + (ay * bw) + (az * bx);
	const z = (aw * bz) + (ax * by) - (ay * bx) + (az * bw);
	const w = (aw * bw) - (ax * bx) - (ay * by) - (az * bz);
	const length = Math.hypot(x, y, z, w) || 1;
	quaternions[offset] = x / length;
	quaternions[offset + 1] = y / length;
	quaternions[offset + 2] = z / length;
	quaternions[offset + 3] = w / length;
}

function getDiceAddictionQuaternionAngle(quaternions, stepA, stepB) {
	const a = stepA * 4;
	const b = stepB * 4;
	const dot = Math.abs(
		quaternions[a] * quaternions[b]
		+ quaternions[a + 1] * quaternions[b + 1]
		+ quaternions[a + 2] * quaternions[b + 2]
		+ quaternions[a + 3] * quaternions[b + 3]
	);
	return 2 * Math.acos(Math.min(Math.max(dot, -1), 1));
}

function measureDiceAddictionBouncyTailMotion(positions, quaternions, endStep) {
	const lookback = Math.min(DICE_ADDICTION_BOUNCY_REVEAL_LOOKBACK, endStep);
	if (lookback < 4) return 0;
	const startStep = endStep - lookback;
	let path = 0;
	let angular = 0;
	for (let step = startStep + 1; step <= endStep; step++) {
		const current = step * 3;
		const previous = (step - 1) * 3;
		const dx = positions[current] - positions[previous];
		const dy = positions[current + 1] - positions[previous + 1];
		const dz = positions[current + 2] - positions[previous + 2];
		path += Math.hypot(dx, dy, dz);
		angular += getDiceAddictionQuaternionAngle(quaternions, step - 1, step);
	}
	return Math.min(1, path / 0.42 + angular / (Math.PI * 5.5));
}

function getDiceAddictionBouncyRevealExtension(positions, quaternions, endStep) {
	const motion = measureDiceAddictionBouncyTailMotion(positions, quaternions, endStep);
	return Math.round(
		DICE_ADDICTION_BOUNCY_REVEAL_MIN_EXTENSION
		+ motion * (DICE_ADDICTION_BOUNCY_REVEAL_MAX_EXTENSION - DICE_ADDICTION_BOUNCY_REVEAL_MIN_EXTENSION)
	);
}

function sampleDiceAddictionVectorBuffer(source, stride, sourceStep) {
	const maxStep = Math.floor(source.length / stride) - 1;
	const lowStep = Math.min(Math.max(Math.floor(sourceStep), 0), maxStep);
	const highStep = Math.min(lowStep + 1, maxStep);
	const t = sourceStep - lowStep;
	const low = lowStep * stride;
	const high = highStep * stride;
	const values = new Array(stride);
	for (let i = 0; i < stride; i++) {
		values[i] = source[low + i] * (1 - t) + source[high + i] * t;
	}
	return values;
}

function sampleDiceAddictionQuaternionBuffer(source, sourceStep) {
	const lowStep = Math.min(Math.max(Math.floor(sourceStep), 0), Math.floor(source.length / 4) - 1);
	const highStep = Math.min(lowStep + 1, Math.floor(source.length / 4) - 1);
	const t = sourceStep - lowStep;
	const low = lowStep * 4;
	const high = highStep * 4;
	let dot = source[low] * source[high]
		+ source[low + 1] * source[high + 1]
		+ source[low + 2] * source[high + 2]
		+ source[low + 3] * source[high + 3];
	const sign = dot < 0 ? -1 : 1;
	const x = source[low] * (1 - t) + source[high] * sign * t;
	const y = source[low + 1] * (1 - t) + source[high + 1] * sign * t;
	const z = source[low + 2] * (1 - t) + source[high + 2] * sign * t;
	const w = source[low + 3] * (1 - t) + source[high + 3] * sign * t;
	const length = Math.hypot(x, y, z, w) || 1;
	return [x / length, y / length, z / length, w / length];
}

function stretchDiceAddictionBouncyRevealTail(simResult, index, extensionSteps) {
	const positions = new Float32Array(simResult.positionsBuffers[index]);
	const quaternions = new Float32Array(simResult.quaternionsBuffers[index]);
	const originalEnd = Math.min(
		Number(simResult.iterationsNeeded) || 0,
		Math.floor(positions.length / 3) - 1,
		Math.floor(quaternions.length / 4) - 1
	);
	if (originalEnd < 12 || extensionSteps <= 0) return originalEnd;
	const newEnd = originalEnd + extensionSteps;
	const tailLength = Math.min(originalEnd, Math.max(42, Math.min(118, Math.round(extensionSteps * 1.15))));
	const tailStart = Math.max(0, originalEnd - tailLength);
	const newPositions = new Float32Array((newEnd + 1) * 3);
	const newQuaternions = new Float32Array((newEnd + 1) * 4);

	newPositions.set(positions.slice(0, (tailStart + 1) * 3));
	newQuaternions.set(quaternions.slice(0, (tailStart + 1) * 4));
	for (let step = tailStart + 1; step <= newEnd; step++) {
		const t = (step - tailStart) / Math.max(newEnd - tailStart, 1);
		const eased = 1 - Math.pow(1 - t, 1.35);
		const sourceStep = tailStart + eased * (originalEnd - tailStart);
		const pos = sampleDiceAddictionVectorBuffer(positions, 3, sourceStep);
		const quat = sampleDiceAddictionQuaternionBuffer(quaternions, sourceStep);
		newPositions.set(pos, step * 3);
		newQuaternions.set(quat, step * 4);
	}

	simResult.positionsBuffers[index] = newPositions.buffer;
	simResult.quaternionsBuffers[index] = newQuaternions.buffer;
	simResult._diceAddictionBouncyExtendedEnds ||= {};
	simResult._diceAddictionBouncyExtendedEnds[index] = newEnd;
	if (Array.isArray(simResult.deads) && simResult.deads[index]) simResult.deads[index] = Math.max(simResult.deads[index], newEnd + 1);
	return newEnd;
}

function holdDiceAddictionSimulationBufferToStep(simResult, index, newEnd) {
	const positions = new Float32Array(simResult.positionsBuffers[index]);
	const quaternions = new Float32Array(simResult.quaternionsBuffers[index]);
	const bufferEnd = Math.min(Math.floor(positions.length / 3) - 1, Math.floor(quaternions.length / 4) - 1);
	const intendedEnd = simResult._diceAddictionBouncyExtendedEnds?.[index] ?? (Number(simResult.iterationsNeeded) || 0);
	const currentEnd = Math.min(
		intendedEnd,
		bufferEnd
	);
	if (currentEnd >= newEnd) return false;
	const newPositions = new Float32Array((newEnd + 1) * 3);
	const newQuaternions = new Float32Array((newEnd + 1) * 4);
	newPositions.set(positions.slice(0, (currentEnd + 1) * 3));
	newQuaternions.set(quaternions.slice(0, (currentEnd + 1) * 4));
	const finalPosition = sampleDiceAddictionVectorBuffer(positions, 3, currentEnd);
	const finalQuaternion = sampleDiceAddictionQuaternionBuffer(quaternions, currentEnd);
	for (let step = currentEnd + 1; step <= newEnd; step++) {
		newPositions.set(finalPosition, step * 3);
		newQuaternions.set(finalQuaternion, step * 4);
	}
	simResult.positionsBuffers[index] = newPositions.buffer;
	simResult.quaternionsBuffers[index] = newQuaternions.buffer;
	if (Array.isArray(simResult.deads) && simResult.deads[index]) simResult.deads[index] = Math.max(simResult.deads[index], newEnd + 1);
	return true;
}

function applyDiceAddictionBouncyVisualRollSpin(simResult, index, contacts, stepCount) {
	if (!simResult?.quaternionsBuffers?.[index] || !contacts.length) return false;
	const quaternions = new Float32Array(simResult.quaternionsBuffers[index]);
	const quaternionStepCount = Math.min(stepCount, Math.floor(quaternions.length / 4));
	let changed = false;
	for (let contactIndex = 0; contactIndex < contacts.length; contactIndex++) {
		const contactStep = contacts[contactIndex];
		const turns = DICE_ADDICTION_BOUNCY_VISUAL_SPIN_TURNS[contactIndex] || 0;
		const fullTurns = turns >= 0.95 ? 2 : 0;
		if (fullTurns <= 0) continue;
		const duration = Math.min(DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION, quaternionStepCount - contactStep - 1);
		if (duration < 8) continue;
		const axis = normalizeDiceAddictionVector3({
			x: 0.72 + contactIndex * 0.17,
			y: 0.38 + contactIndex * 0.11,
			z: 0.58 - contactIndex * 0.05
		});
		for (let offset = 0; offset <= duration; offset++) {
			const step = contactStep + offset;
			if (step >= quaternionStepCount) break;
			const t = offset / duration;
			const eased = 1 - Math.pow(1 - t, 2.35);
			const angle = eased * fullTurns * Math.PI * 2;
			multiplyDiceAddictionQuaternionByAxisAngle(quaternions, step, axis, angle);
			changed = true;
		}
	}
	return changed;
}

function applyDiceAddictionBouncyVisualGroundBounces(simResult, bouncyIds) {
	if (!simResult?.ids || !simResult?.positionsBuffers || !bouncyIds?.size) return false;

	let changed = false;
	let extendedIterationsNeeded = Number(simResult.iterationsNeeded) || 0;
	for (let index = 0; index < simResult.ids.length; index++) {
		if (!bouncyIds.has(String(simResult.ids[index]))) continue;
		if (!simResult.positionsBuffers[index]) continue;
		const positions = new Float32Array(simResult.positionsBuffers[index]);
		const stepCount = Math.floor(positions.length / 3);
		if (stepCount < DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION + 8) continue;

		let minY = Infinity;
		let maxY = -Infinity;
		for (let step = 0; step < stepCount; step++) {
			const y = positions[step * 3 + 1];
			if (!Number.isFinite(y)) continue;
			minY = Math.min(minY, y);
			maxY = Math.max(maxY, y);
		}
		if (!Number.isFinite(minY) || !Number.isFinite(maxY)) continue;

		const groundWindow = Math.max(150 * DICE_ADDICTION_LEGACY_TO_METERS, (maxY - minY) * 0.08);
		const contacts = [];
		let cooldownUntil = 0;
		for (let step = 2; step < stepCount - DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION - 2; step++) {
			if (step < cooldownUntil) continue;
			const prevY = positions[(step - 1) * 3 + 1];
			const y = positions[step * 3 + 1];
			const nextY = positions[(step + 1) * 3 + 1];
			if (y <= minY + groundWindow && prevY > y && nextY >= y) {
				contacts.push(step);
				cooldownUntil = step + DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION;
				if (contacts.length >= DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_HEIGHTS.length) break;
			}
		}
		if (contacts.length < DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_HEIGHTS.length) {
			cooldownUntil = 0;
			for (let step = 6; step < stepCount - DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION - 2; step++) {
				if (step < cooldownUntil || contacts.some((contact) => Math.abs(contact - step) < DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION)) continue;
				const previousHigh = positions[(step - 6) * 3 + 1] > minY + groundWindow;
				const y = positions[step * 3 + 1];
				if (previousHigh && y <= minY + groundWindow) {
					contacts.push(step);
					contacts.sort((a, b) => a - b);
					cooldownUntil = step + DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION;
					if (contacts.length >= DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_HEIGHTS.length) break;
				}
			}
		}

		for (let contactIndex = 0; contactIndex < contacts.length; contactIndex++) {
			const contactStep = contacts[contactIndex];
			const height = DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_HEIGHTS[contactIndex];
			for (let offset = 0; offset <= DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION; offset++) {
				const step = contactStep + offset;
				if (step >= stepCount) break;
				const t = offset / DICE_ADDICTION_BOUNCY_VISUAL_BOUNCE_DURATION;
				positions[step * 3 + 1] += Math.sin(Math.PI * t) * height;
			}
			changed = true;
		}
		changed = applyDiceAddictionBouncyVisualRollSpin(simResult, index, contacts, stepCount) || changed;
		if (simResult.quaternionsBuffers?.[index]) {
			const updatedPositions = new Float32Array(simResult.positionsBuffers[index]);
			const updatedQuaternions = new Float32Array(simResult.quaternionsBuffers[index]);
			const endStep = Math.min(
				extendedIterationsNeeded,
				Math.floor(updatedPositions.length / 3) - 1,
				Math.floor(updatedQuaternions.length / 4) - 1
			);
			const extensionSteps = getDiceAddictionBouncyRevealExtension(updatedPositions, updatedQuaternions, endStep);
			const newEnd = stretchDiceAddictionBouncyRevealTail(simResult, index, extensionSteps);
			if (newEnd > extendedIterationsNeeded) extendedIterationsNeeded = newEnd;
			changed = true;
		}
	}
	if (extendedIterationsNeeded > (Number(simResult.iterationsNeeded) || 0)) {
		for (let index = 0; index < simResult.ids.length; index++) {
			if (!simResult.positionsBuffers[index] || !simResult.quaternionsBuffers?.[index]) continue;
			holdDiceAddictionSimulationBufferToStep(simResult, index, extendedIterationsNeeded);
		}
		simResult.iterationsNeeded = extendedIterationsNeeded;
		changed = true;
	}
	return changed;
}

