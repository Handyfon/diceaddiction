function collectDiceAddictionPresetData(root = document) {
	const controls = getDiceAddictionPresetControls(root);
	return {
		version: 1,
		fields: controls.map((control) => ({
			name: control.name,
			tag: control.tagName.toLowerCase(),
			type: String(control.type || "").toLowerCase(),
			value: getDiceAddictionPresetControlValue(control),
			checked: ["checkbox", "radio"].includes(String(control.type || "").toLowerCase()) ? !!control.checked : undefined
		}))
	};
}

function applyDiceAddictionPresetData(root = document, preset) {
	const fields = Array.isArray(preset?.data?.fields) ? preset.data.fields : [];
	if (!fields.length) return false;

	diceAddictionPresetApplyInProgress = true;
	try {
		moveDiceAddictionEmissionSettingsInline(root);
		addDiceAddictionMaterialOptions(root);
		let controls = getDiceAddictionPresetControls(root);
		let controlsByName = controls.reduce((map, control) => {
			if (!map.has(control.name)) map.set(control.name, []);
			map.get(control.name).push(control);
			return map;
		}, new Map());

		for (const field of fields) {
			const matchingControls = controlsByName.get(field.name) || [];
			if (!matchingControls.some((control) => control.matches?.("[data-system]"))) continue;
			for (const control of matchingControls) {
				setDiceAddictionPresetControlValue(control, field);
				updateDiceAddictionRangeLabel(control);
			}
		}

		rebuildDiceAddictionPresetSystemSettings(root);
		moveDiceAddictionEmissionSettingsInline(root);
		addDiceAddictionMaterialOptions(root);
		controls = getDiceAddictionPresetControls(root);
		controlsByName = controls.reduce((map, control) => {
			if (!map.has(control.name)) map.set(control.name, []);
			map.get(control.name).push(control);
			return map;
		}, new Map());

		for (const field of fields) {
			const matchingControls = controlsByName.get(field.name) || [];
			for (const control of matchingControls) {
				setDiceAddictionPresetControlValue(control, field);
				updateDiceAddictionRangeLabel(control);
			}
		}
		refreshDiceAddictionPresetDialogState(root);
	} finally {
		diceAddictionPresetApplyInProgress = false;
	}
	return true;
}

