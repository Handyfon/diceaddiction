function createDiceAddictionJellyPuddle(box, dice, position, strength = 0.5, offset = null, options = {}) {
	const THREE = diceAddictionThree || box?.diceScene?.constructor?.THREE || game?.dice3d?.DiceFactory?._diceAddictionThree;
	const scene = box?.scene || box?.diceScene?.scene || dice?.parent?.parent;
	if (!THREE?.CircleGeometry || !THREE?.Mesh || !scene?.add) return false;

	const sourceMaterial = getDiceAddictionJellyPuddleMaterialSource(dice);
	const color = boostDiceAddictionJellyPuddleColor(THREE, options.color?.clone?.() || resolveDiceAddictionJellyPuddleColor(THREE, sourceMaterial));
	const texture = options.texture === false ? null : getDiceAddictionJellyPuddleTexture(THREE, sourceMaterial);
	const maps = getDiceAddictionJellyPuddleMaps(THREE);
	const opacity = Math.min(0.46, 0.18 + strength * 0.24);
	const radius = (24 + Math.random() * 18 + strength * 24) * DICE_ADDICTION_LEGACY_TO_METERS;
	const geometry = getDiceAddictionJellyPuddleGeometry(THREE);
	if (!geometry) return false;
	const material = createDiceAddictionJellyPuddleMaterial(THREE, color, opacity, maps, texture);

	const puddle = new THREE.Mesh(geometry, material);
	puddle.name = "Dice Addiction Jelly Puddle";
	puddle.renderOrder = 4;
	puddle.rotation.x = -Math.PI / 2;
	puddle.position.set(
		position.x + (offset?.x || 0),
		0.004 + Math.random() * 0.003,
		position.z + (offset?.z || 0)
	);
	const roundness = 0.88 + Math.random() * 0.24;
	const targetScaleX = roundness * radius;
	const targetScaleY = (0.9 + Math.random() * 0.18) * radius;
	puddle.scale.set(targetScaleX * 0.56, targetScaleY * 0.56, 1);
	puddle.rotation.z = Math.random() * Math.PI;
	const rim = createDiceAddictionJellyPuddleRim(THREE, geometry, color, maps, opacity);
	if (rim) {
		rim.position.copy?.(puddle.position);
		rim.position.y += 0.0025;
		rim.scale.copy?.(puddle.scale);
		rim.rotation.z = puddle.rotation.z;
	}
	puddle.userData.diceAddictionJellyPuddle = {
		created: performance.now(),
		ttl: DICE_ADDICTION_JELLY_PUDDLE_TTL,
		baseOpacity: material.opacity,
		rim,
		rimBaseOpacity: rim?.material?.opacity || 0,
		texture,
		alphaMap: maps?.alphaMap || null,
		normalMap: maps?.normalMap || null,
		rimAlphaMap: maps?.rimAlphaMap || null,
		targetScaleX,
		targetScaleY,
		sharedGeometry: true,
		disposeTexture: !!texture?.image?.getContext,
		disposeMaps: false
	};

	box._diceAddictionJellyPuddles ||= [];
	box._diceAddictionJellyPuddles.push(puddle);
	while (box._diceAddictionJellyPuddles.length > DICE_ADDICTION_JELLY_PUDDLE_MAX) {
		const oldPuddle = box._diceAddictionJellyPuddles.shift();
		const oldState = oldPuddle?.userData?.diceAddictionJellyPuddle;
		oldPuddle?.parent?.remove?.(oldPuddle);
		oldState?.rim?.parent?.remove?.(oldState.rim);
		if (!oldState?.sharedGeometry) oldPuddle?.geometry?.dispose?.();
		if (oldState?.disposeTexture) oldState.texture?.dispose?.();
		if (oldState?.disposeMaps) {
			oldState.alphaMap?.dispose?.();
			oldState.normalMap?.dispose?.();
			oldState.rimAlphaMap?.dispose?.();
		}
		oldPuddle?.material?.dispose?.();
		if (!oldState?.sharedGeometry) oldState?.rim?.geometry?.dispose?.();
		oldState?.rim?.material?.dispose?.();
	}
	scene.add(puddle);
	if (rim) scene.add(rim);
	return true;
}

function updateDiceAddictionJellyPuddles(box, now = performance.now()) {
	const puddles = box?._diceAddictionJellyPuddles;
	let active = updateDiceAddictionJellyDroplets(box, now);
	if (!Array.isArray(puddles) || !puddles.length) return active;
	for (let i = puddles.length - 1; i >= 0; i--) {
		const puddle = puddles[i];
		const state = puddle?.userData?.diceAddictionJellyPuddle;
		if (!puddle || !state) {
			puddles.splice(i, 1);
			continue;
		}
		const age = now - state.created;
		const t = Math.max(0, Math.min(1, age / state.ttl));
		if (t >= 1) {
			puddle.parent?.remove?.(puddle);
			state.rim?.parent?.remove?.(state.rim);
			if (!state.sharedGeometry) puddle.geometry?.dispose?.();
			if (state.disposeTexture) state.texture?.dispose?.();
			if (state.disposeMaps) {
				state.alphaMap?.dispose?.();
				state.normalMap?.dispose?.();
				state.rimAlphaMap?.dispose?.();
			}
			puddle.material?.dispose?.();
			if (!state.sharedGeometry) state.rim?.geometry?.dispose?.();
			state.rim?.material?.dispose?.();
			puddles.splice(i, 1);
			continue;
		}
		const splat = clampDiceAddictionUnit(age / 180);
		const settle = clampDiceAddictionUnit((age - 180) / 260);
		const pop = 0.56 + diceAddictionSmoothstep(0, 1, splat) * 0.56 - diceAddictionSmoothstep(0, 1, settle) * 0.12;
		const drift = 1 + 0.08 * diceAddictionSmoothstep(0, 1, t);
		puddle.scale.x = state.targetScaleX * pop * drift;
		puddle.scale.y = state.targetScaleY * pop * drift;
		puddle.material.opacity = state.baseOpacity * (1 - t) * (1 - t * 0.35);
		if (state.rim) {
			state.rim.scale.copy?.(puddle.scale);
			state.rim.material.opacity = state.rimBaseOpacity * (1 - t) * (1 - t);
			state.rim.visible = state.rim.material.opacity > 0.01;
		}
		puddle.visible = puddle.material.opacity > 0.01;
		active = true;
	}
	return active;
}

function createDiceAddictionJellyDropletMaterial(THREE, color) {
	const options = {
		color,
		transparent: true,
		opacity: 0.74,
		roughness: 0.12,
		metalness: 0,
		envMapIntensity: 1.4
	};
	if (THREE.MeshPhysicalMaterial) {
		return new THREE.MeshPhysicalMaterial({
			...options,
			clearcoat: 1,
			clearcoatRoughness: 0.04,
			transmission: 0.22,
			thickness: 0.02
		});
	}
	if (THREE.MeshStandardMaterial) return new THREE.MeshStandardMaterial(options);
	return new THREE.MeshBasicMaterial(options);
}

function getDiceAddictionWorldPosition(THREE, dice) {
	const position = new THREE.Vector3();
	const object = dice?.parent && dice.parent.type !== "Scene" ? dice.parent : dice;
	if (object?.getWorldPosition) object.getWorldPosition(position);
	else if (object?.position) position.copy(object.position);
	return position;
}

function getDiceAddictionJellyImpactDirection(THREE, dice) {
	const direction = new THREE.Vector3(0, 0.45, 1);
	const state = dice?.userData?.diceAddictionJelly;
	const position = getDiceAddictionWorldPosition(THREE, dice);
	if (state?.lastPosition?.clone) {
		direction.copy(position).sub(state.lastPosition);
	}
	if (direction.lengthSq?.() < 0.000001 && state?.direction) {
		direction.set(state.direction.x || 0, state.direction.y || 0, state.direction.z || 0);
	}
	if (Math.abs(direction.x) + Math.abs(direction.z) < 0.0001) {
		direction.set((Math.random() - 0.5) * 0.4, 0.35, (Math.random() - 0.5) * 0.4);
	}
	direction.y = Math.max(0.12, Math.abs(direction.y || 0) * 0.25);
	return direction.normalize();
}

function spawnDiceAddictionJellyDroplets(box, dice, position, strength, color, options = {}) {
	const THREE = diceAddictionThree || box?.diceScene?.constructor?.THREE || game?.dice3d?.DiceFactory?._diceAddictionThree;
	const scene = box?.scene || box?.diceScene?.scene || dice?.parent?.parent;
	if (!THREE?.SphereGeometry || !THREE?.Mesh || !scene?.add) return false;

	box._diceAddictionJellyDroplets ||= [];
	const countMultiplier = Number(options.countMultiplier) || 1;
	const minCount = Number(options.minCount) || 2;
	const maxCount = Number(options.maxCount) || 18;
	const count = Math.max(minCount, Math.min(maxCount, Math.round((2 + strength * 8 + Math.random() * 4) * countMultiplier)));
	const geometry = getDiceAddictionJellyDropletGeometry(THREE);
	if (!geometry) return false;
	for (let i = 0; i < count; i++) {
		const radius = ((options.visualOnly ? 3.5 : 5) + Math.random() * (options.visualOnly ? 6 : 7) + strength * (options.visualOnly ? 3 : 4)) * DICE_ADDICTION_LEGACY_TO_METERS;
		const material = createDiceAddictionJellyDropletMaterial(THREE, color);
		const droplet = new THREE.Mesh(geometry, material);
		droplet.scale.setScalar?.(radius);
		const baseDirection = options.direction;
		const baseAngle = baseDirection ? Math.atan2(baseDirection.z || 0, baseDirection.x || 0) : Math.random() * Math.PI * 2;
		const spreadAngle = Number(options.spreadAngle) || Math.PI * 2;
		const angle = baseDirection ? baseAngle + (Math.random() - 0.5) * spreadAngle : Math.random() * Math.PI * 2;
		const spread = (8 + Math.random() * (options.visualOnly ? 54 : 30)) * DICE_ADDICTION_LEGACY_TO_METERS;
		const upward = options.visualOnly ? 35 + Math.random() * 85 + strength * 60 : 60 + Math.random() * 80 + strength * 80;
		droplet.position.set(
			position.x + Math.cos(angle) * spread,
			Math.max(position.y || 0, 0) + upward * DICE_ADDICTION_LEGACY_TO_METERS,
			position.z + Math.sin(angle) * spread
		);
		const sideVelocity = (0.045 + Math.random() * (options.visualOnly ? 0.16 : 0.07)) * strength;
		droplet.userData.diceAddictionJellyDroplet = {
			created: performance.now(),
			dice,
			color: color.clone?.() || color,
			strength: Math.max(0.08, strength * (0.18 + Math.random() * 0.28)),
			visualOnly: !!options.visualOnly,
			ttl: options.ttl || (options.visualOnly ? DICE_ADDICTION_JELLY_SPLAT_PARTICLE_TTL : DICE_ADDICTION_JELLY_DROPLET_TTL),
			velocity: {
				x: Math.cos(angle) * sideVelocity,
				y: (options.visualOnly ? 0.045 : 0.13) + Math.random() * (options.visualOnly ? 0.14 : 0.24) + strength * 0.08,
				z: Math.sin(angle) * sideVelocity
			},
			sharedGeometry: true,
			lastTime: performance.now()
		};
		box._diceAddictionJellyDroplets.push(droplet);
		scene.add(droplet);
	}

	while (box._diceAddictionJellyDroplets.length > DICE_ADDICTION_JELLY_DROPLET_MAX) {
		const oldDroplet = box._diceAddictionJellyDroplets.shift();
		const oldState = oldDroplet?.userData?.diceAddictionJellyDroplet;
		oldDroplet?.parent?.remove?.(oldDroplet);
		if (!oldState?.sharedGeometry) oldDroplet?.geometry?.dispose?.();
		oldDroplet?.material?.dispose?.();
	}
	return true;
}

function updateDiceAddictionJellyDroplets(box, now = performance.now()) {
	const droplets = box?._diceAddictionJellyDroplets;
	if (!Array.isArray(droplets) || !droplets.length) return false;
	let active = false;
	for (let i = droplets.length - 1; i >= 0; i--) {
		const droplet = droplets[i];
		const state = droplet?.userData?.diceAddictionJellyDroplet;
		if (!droplet || !state) {
			droplets.splice(i, 1);
			continue;
		}
		const dt = Math.min(Math.max((now - (state.lastTime || now)) / 1000, 1 / 120), 1 / 30);
		state.lastTime = now;
		state.velocity.y -= DICE_ADDICTION_JELLY_DROPLET_GRAVITY * dt;
		droplet.position.x += state.velocity.x * dt;
		droplet.position.y += state.velocity.y * dt;
		droplet.position.z += state.velocity.z * dt;
		const age = now - state.created;
		const ttl = state.ttl || DICE_ADDICTION_JELLY_DROPLET_TTL;
		if (droplet.position.y <= 0.006 || age > ttl) {
			if (!state.visualOnly) {
				createDiceAddictionJellyPuddle(box, state.dice, droplet.position, state.strength, null, {
					color: state.color,
					texture: false
				});
			}
			droplet.parent?.remove?.(droplet);
			if (!state.sharedGeometry) droplet.geometry?.dispose?.();
			droplet.material?.dispose?.();
			droplets.splice(i, 1);
			active = true;
			continue;
		}
		droplet.material.opacity = Math.max(0, 0.74 * (1 - age / ttl));
		active = true;
	}
	return active;
}

function spawnDiceAddictionJellyCollisionSplats(box, strength = 0.5, source = "table") {
	const now = performance.now();
	if (!box || now - (box._diceAddictionLastCollisionSplat || 0) < DICE_ADDICTION_JELLY_COLLISION_SPLAT_COOLDOWN) return false;
	const THREE = diceAddictionThree || box?.diceScene?.constructor?.THREE || game?.dice3d?.DiceFactory?._diceAddictionThree;
	if (!THREE) return false;
	const jellyDice = collectDiceAddictionLiveDice(box).filter((die) => isDiceAddictionJellySoundDice(die) || hasDiceAddictionJellyMaterial(die));
	if (!jellyDice.length) return false;

	box._diceAddictionLastCollisionSplat = now;
	const normalizedStrength = Math.max(0.15, Math.min(1, Number(strength) || 0.5));
	const count = source === "dice" ? Math.min(2, jellyDice.length) : 1;
	for (let i = 0; i < count; i++) {
		const dice = jellyDice[(Math.floor(Math.random() * jellyDice.length) + i) % jellyDice.length];
		const sourceMaterial = getDiceAddictionJellyPuddleMaterialSource(dice);
		const color = boostDiceAddictionJellyPuddleColor(THREE, resolveDiceAddictionJellyPuddleColor(THREE, sourceMaterial));
		const position = getDiceAddictionWorldPosition(THREE, dice);
		const direction = getDiceAddictionJellyImpactDirection(THREE, dice);
		if (source !== "table") position.y += (45 + Math.random() * 95) * DICE_ADDICTION_LEGACY_TO_METERS;
		spawnDiceAddictionJellyDroplets(box, dice, position, normalizedStrength, color, {
			visualOnly: true,
			direction,
			spreadAngle: source === "dice" ? Math.PI * 0.85 : Math.PI * 0.62,
			countMultiplier: source === "dice" ? 0.95 : 0.7,
			minCount: source === "dice" ? 5 : 3,
			maxCount: source === "dice" ? 12 : 8,
			ttl: DICE_ADDICTION_JELLY_SPLAT_PARTICLE_TTL + Math.random() * 180
		});
	}
	return true;
}

function maybeCreateDiceAddictionJellyPuddle(box, dice, position, verticalVelocity, speed, now) {
	const state = dice?.userData?.diceAddictionJelly;
	if (!box || !state || now - (state.lastPuddleTime || 0) < DICE_ADDICTION_JELLY_PUDDLE_COOLDOWN) return false;
	const previousVelocityY = Number(state.lastVelocityY) || 0;
	const bouncedUp = previousVelocityY < -DICE_ADDICTION_JELLY_PUDDLE_MIN_FALL_SPEED && verticalVelocity > 0.025;
	if (!bouncedUp) return false;

	const strength = Math.max(0.18, Math.min(1, Math.abs(previousVelocityY) * 1.4 + speed * 0.04));
	const count = 1 + (Math.random() < strength * 0.85 ? 1 : 0) + (Math.random() < strength * 0.45 ? 1 : 0);
	let created = false;
	for (let i = 0; i < count; i++) {
		const spread = (i === 0 ? 0 : (18 + Math.random() * 42) * DICE_ADDICTION_LEGACY_TO_METERS);
		const angle = Math.random() * Math.PI * 2;
		const offset = {
			x: Math.cos(angle) * spread,
			z: Math.sin(angle) * spread
		};
		created = createDiceAddictionJellyPuddle(box, dice, position, strength * (i === 0 ? 1 : 0.45 + Math.random() * 0.28), offset) || created;
	}
	if (!created) return false;
	const THREE = diceAddictionThree || box?.diceScene?.constructor?.THREE || game?.dice3d?.DiceFactory?._diceAddictionThree;
	const sourceMaterial = getDiceAddictionJellyPuddleMaterialSource(dice);
	const splashColor = THREE ? resolveDiceAddictionJellyPuddleColor(THREE, sourceMaterial) : null;
	if (splashColor && Math.random() < 0.9) spawnDiceAddictionJellyDroplets(box, dice, position, strength, splashColor);
	state.lastPuddleTime = now;
	return true;
}

function nudgeDiceAddictionJellyDragDice(dice, direction, pointerDistance = 0) {
	markDiceAddictionJellyDice(dice);
	const state = dice?.userData?.diceAddictionJelly;
	if (!state) return false;

	const length = Math.hypot(direction?.x || 0, direction?.y || 0, direction?.z || 0);
	if (length > 0.001) {
		state.direction = {
			x: direction.x / length,
			y: direction.y / length,
			z: direction.z / length
		};
	}

	const boost = Math.min(0.34, 0.075 + pointerDistance * 0.0045);
	state.dragWobble = Math.max(state.dragWobble || 0, boost);
	state.wobble = Math.max(state.wobble || 0, boost * 0.72);
	state.dragUntil = performance.now() + 260;
	state.restWobble = Math.max(state.restWobble || 0, boost * 0.28);
	state.restUntil = performance.now() + 1400;
	return true;
}

